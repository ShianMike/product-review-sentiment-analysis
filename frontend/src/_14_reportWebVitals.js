const reportWebVitals = onPerfEntry => {
  // CRA keeps performance measurement optional. Passing a callback here lets
  // us send Core Web Vitals somewhere later without changing the app startup.
  if (onPerfEntry && onPerfEntry instanceof Function) {
    import('web-vitals').then(({ getCLS, getFID, getFCP, getLCP, getTTFB }) => {
      getCLS(onPerfEntry);
      getFID(onPerfEntry);
      getFCP(onPerfEntry);
      getLCP(onPerfEntry);
      getTTFB(onPerfEntry);
    });
  }
};

export default reportWebVitals;
